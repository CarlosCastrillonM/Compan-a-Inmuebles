package umag.datos;

/**
 *
 * @author carla
 */
public class Cliente {
    private int id;
    private int edad;
    private String nombre;
    private String sexo;

    public Cliente(int id, int edad, String nombre, String sexo) {
        this.id = id;
        this.edad = edad;
        this.nombre = nombre;
        this.sexo = sexo;
    }
    

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the edad
     */
    public int getEdad() {
        return edad;
    }

    /**
     * @param edad the edad to set
     */
    public void setEdad(int edad) {
        this.edad = edad;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the sexo
     */
    public String getSexo() {
        return sexo;
    }

    /**
     * @param sexo the sexo to set
     */
    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    @Override
    public String toString() {
        return "Cliente{" + "id=" + id + ", edad=" + edad + ", nombre=" + nombre + ", sexo=" + sexo + '}';
    }
    
}
